package menu;

public class MenuJugador {

	// Metodo que da la interfaz de los movimientos
	/**
	 * Clase MenuJugador
	 * 
	 * Contiene las teclas de los movimientos
	 * 
	 * @version 1.0
	 * @author Roberto Ruiz
	 * 
	 */
	
	/**
	 * Metodo que devuelve la interfaz de los posibles movimientos del jugador
	 * 
	 * @return devuelve la interfaz del jugador
	 *
	 */
	public String getInterfazMovimientos() {
		return "       -----\n" + "       | W |\n" + "       -----\n" + "-----  -----  -----\n"
				+ "| A |  | S |  | D |\n" + "-----  -----  -----\n";
	}

}
